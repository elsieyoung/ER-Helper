package com.example.triage;

@SuppressWarnings("serial")
public class PatientNotFoundException extends Exception {
	/**
	 * An exception class defining an exception when the patient of interest
	 * cannot be found in the database. It will be thrown when the user input an
	 * invalid health card number.
	 */

	public PatientNotFoundException(String message) {
		super(message);
	}

	public PatientNotFoundException(String message, Throwable throwable) {
		super(message, throwable);
	}
}
